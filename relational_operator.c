#include<stdio.h>

int main () {

    int x,y;
    printf("enter two value\n");
    scanf("%d%d",&x,&y);

  //  relational operators equal(==), notequal(!=), greater then(>), less then(<),greater then equal(>=), less then equal(<=)

  if(x==y){
    printf("true\n");
  }
  else{
    printf("false\n");
  }

 if(x!=y){
    printf("true\n");
  }
  else{
    printf("false\n");
  }

 if(x>y){
    printf("true\n");
  }
  else{
    printf("false\n");
  }

 if(x<y){
    printf("true\n");
  }
  else{
    printf("false\n");
  }


 if(x>=y){
    printf("true\n");
  }
  else{
    printf("false\n");
  }

 if(x<=y){
    printf("true\n");
  }
  else{
    printf("false\n");
  }

  return 0;



}